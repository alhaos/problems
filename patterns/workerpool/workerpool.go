package workerpool

import (
	"context"
	"sync"
)

// WorkerPool реализация шаблона Worker pool
// Прерывает выполнение при завершении контекста
func WorkerPool(ctx context.Context, numWorkers int, jobs <-chan int) <-chan int {

	// Создаём канал куда все воркеры будут писать результаты. Один канал на всех.
	out := make(chan int)

	// Счётчик живых горутин. Нужен чтобы узнать когда все воркеры завершились.
	var wg sync.WaitGroup

	// Запускаем цикл запуска воркеров
	for range numWorkers {

		// Устанавливаем в waitGroup то, что воркер начал работу и нужно ждать его завершения
		wg.Add(1)

		// Запускаем воркера
		go func() {

			// После выполнения фиксируем в waitGroup, что воркер завершил работу
			defer wg.Done()

			// Читаем из канала jobs используя синтаксис цикла range
			// Все воркеры читают из одного jobs
			for job := range jobs {

				// select блокирует выполнение горутины до наступления одного из возможных событий
				select {

				// На стороне канала out кто-то готов читать значение
				case out <- job * job:

				// Контекст выполнения завершен
				case <-ctx.Done():

					// Прерываем выполнение воркера
					// сработает defer wg.Done()
					return
				}
			}
		}()
	}

	// Отдельная горутина-наблюдатель. wg.Wait() блокируется пока счётчик не станет 0 — то есть пока все воркеры не завершатся.
	// Только тогда закрываем out — получатель узнает что данных больше не будет.
	// В отдельной горутине — если написать wg.Wait() прямо здесь, функция заблокируется и никогда не дойдёт до return out.
	// А воркеры не завершатся пока никто не читает из out. Дедлок.
	go func() {
		wg.Wait()
		close(out)
	}()

	// Возвращаем канал
	return out
}
